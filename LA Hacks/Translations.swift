    // MARK: - Lookup

    static func lookup(key: String, language: String) -> String? {
        if let dict = data[language]     { return dict[key] }
        // For non-Chinese languages fall back to the 2-char prefix (e.g. "pt-BR" → "pt")
        if !language.hasPrefix("zh") {
            let prefix = String(language.prefix(2))
            if let dict = data[prefix]  { return dict[key] }
        }
        return nil
    }

